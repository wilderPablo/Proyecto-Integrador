package modelo.dto;

/**
 *
 * @author berna
 */
public class AlmacenamientoDTO {
    private int idProducto;
    private String marca;
    private String modelo;
    private String factForm;
    private String numeroParte;
    private String interfaz;
    private String capacidad;
    private String lectSecuencial;
    private String nombre;
    private double precio;
    private String imagen;

    public int getIdProducto() {
        return idProducto;
    }

    public void setIdProducto(int idProducto) {
        this.idProducto = idProducto;
    }

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public String getNumeroParte() {
        return numeroParte;
    }

    public void setNumeroParte(String numeroParte) {
        this.numeroParte = numeroParte;
    }

    public String getInterfaz() {
        return interfaz;
    }

    public void setInterfaz(String interfaz) {
        this.interfaz = interfaz;
    }

    public String getCapacidad() {
        return capacidad;
    }

    public void setCapacidad(String capacidad) {
        this.capacidad = capacidad;
    }

    public String getLectSecuencial() {
        return lectSecuencial;
    }

    public void setLectSecuencial(String lectSecuencial) {
        this.lectSecuencial = lectSecuencial;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public double getPrecio() {
        return precio;
    }

    public void setPrecio(double precio) {
        this.precio = precio;
    }

    public String getImagen() {
        return imagen;
    }

    public void setImagen(String imagen) {
        this.imagen = imagen;
    }

    public String getModelo() {
        return modelo;
    }

    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    public String getFactForm() {
        return factForm;
    }

    public void setFactForm(String factForm) {
        this.factForm = factForm;
    }
}
